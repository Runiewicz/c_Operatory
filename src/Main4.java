
public class Main4 {

	public static void main(String[] args) {
	int counter = 145;
	System.out.println(counter);
	// Inkrementacja (postinkrementacja)
	System.out.println(counter++);
	System.out.println(counter);
	// Dekrementacja (postdekrementacja)
	System.out.println(counter--);
	System.out.println(counter);
	// Inkrementacja (preinkrementacja)
	System.out.println(++counter);
	System.out.println(counter);
	// Dekrementacja (predekrementacja)
	System.out.println(--counter);
	System.out.println(counter);
	}

}
